    private void tampil(){
        try{
         Connection con = objKoneksi.bukaKoneksi();
         Statement st = con.createStatement();
         String sql = "SELECT * FROM isi";
         ResultSet rs = st.executeQuery(sql);
         while(rs.next()){
             Object[] o;
             o = new Object[6];
             o[0] = rs.getString("no_service");
             o[1] = rs.getString("kd_sp");
             o[2] = rs.getString("harga");
             o[3] = rs.getString("jml_item");
             o[4] = rs.getString("discount");
             o[5] = rs.getString("jml_bayar");
             table.addRow(o);
         }
         con.close();
         st.close();
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
