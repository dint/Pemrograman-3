    private void UbahData(){
        int discount = 0;
        int jml_bayar = 0;
        
        if(rb5.isSelected()){
            discount = 5;
        }
        if(rb10.isSelected()){
            discount = 10;
        }
        if(rb15.isSelected()){
            discount = 15;
        }
        
        jml_bayar = Integer.parseInt(jTextField3.getText()) * Integer.parseInt(jTextField4.getText());
        jml_bayar = jml_bayar - (jml_bayar * discount / 100);
        
        try{
        Connection con = objKoneksi.bukaKoneksi();
        Statement st = con.createStatement();
        String sql = "UPDATE isi SET jml_item = '"+jTextField4.getText()+"', discount = '"+discount+"', jml_bayar = '"+jml_bayar+"' WHERE no_service = '"+jTextField1.getText()+"' and kd_sp = '"+kd_sp+"'"; 
        int sukses = st.executeUpdate(sql);
        if(sukses > 0 ){
            JOptionPane.showMessageDialog(null,"Data Berhasil di Edit","Update",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"Data Tidak Berhasil di Edit");
        }
       con.close();
       st.close();
        }
        catch(SQLException e){
            System.out.println(e.getMessage());
        }
        
    }
